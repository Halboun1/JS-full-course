/* ========== COMPARISON OPERATORS ========== */
'>'                 // greater than
'<'                 // less than
'>='                // greater than or equal to
'<='                // less than or equal to
'=='                // equality
'!='                // not equal
'==='               // strict equality
'!=='               // strict non-equality
// all of those operators will return true or false.


'=='                // checks the equality for value but not the equality of type
'==='               // checks the equality of value and type at the same time.





/* ========== LOGIC FLOW ========== */
'if'                // allows us to run some code if the given condition is true.
'else if'           // allows us to run some code if the given condition is true or else
                    // take the second path.
                



/* ========== TRUTH AND FALSE VALUES ========== */
// FALSE VALUES
'false' , '0', ' ', 'null' , 'undefined' , 'NaN'; // + empty string.
// TRUTH VALUES
// everything else is true



/* ========== LOGICAL OPERATORS ========== */
'&&'                // takes two separate boolean expression and both must be true.
'||'                // takes two separate boolean expression and 1 of them must be true.
'!'                 // negates the result